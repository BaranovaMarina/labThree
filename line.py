# Присвоюємо змінній довільний рядок
text = input("Введіть рядок: ")

# Перевірка, чи довжина рядка достатня
if len(text) < 5:
    print("Рядок занадто короткий для виконання операції.")
else:
    # Визначаємо середину рядка
    middle_index = len(text) // 2

    # Отримуємо кожний 2-й символ, починаючи з 5-го і до середини слова
    result = text[4:middle_index:2]

    # Виводимо результат
    print(f"Результат зрізу: {result}")
